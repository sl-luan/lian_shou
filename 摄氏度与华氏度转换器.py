#华氏度与摄氏度转换器。
"""
需求分析:
华氏度与摄氏度的关系为:T=273+t(K)
"""
a=input("如果你需要将华氏度转化为摄氏度请输入1,如果你需要将摄氏度转化为华氏度请输入2。")
while a!="1" and a!="2":
    a=input("请重新输入，如果你需要将华氏度转化为摄氏度请输入1,否则2。")
if a=="1":
    b=int(input("请输入你需要转化为摄氏度的华氏度数字。"))
    c=273-b
    print("t", c, "℃")
    
elif a=="2":
    d=eval(input("请说你需要转化为华氏度的摄氏度的数字"))
    e=273+d
    print("T为:", e, "K")
        
   